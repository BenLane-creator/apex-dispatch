export function intelligenceLanguage() {
  return document.documentElement.lang.toLowerCase().startsWith("es") ? "es-NI" : "en";
}

export function hasOiTranslation(key, language) {
  return Object.prototype.hasOwnProperty.call(dictionaries[language] || {}, key);
}

export function oiT(key, vars = {}) {
  const language = intelligenceLanguage();
  let text = dictionaries[language]?.[key] ?? dictionaries.en[key] ?? key;
  for (const [name, value] of Object.entries(vars)) {
    text = text.replaceAll(`{${name}}`, String(value));
  }
  return text;
}

export function oiTerm(value, prefix) {
  const normalized = String(value || "").replace(/[-_](.)/g, (_, letter) => letter.toUpperCase());
  const key = `${prefix}${normalized.charAt(0).toUpperCase()}${normalized.slice(1)}`;
  const translated = oiT(key);
  return translated === key ? titleCase(value) : translated;
}

export function applyIntelligenceTranslations(root = document) {
  root.querySelectorAll("[data-oi-i18n]").forEach((element) => {
    element.textContent = oiT(element.dataset.oiI18n);
  });
  root.querySelectorAll("[data-oi-i18n-placeholder]").forEach((element) => {
    element.placeholder = oiT(element.dataset.oiI18nPlaceholder);
  });
  root.querySelectorAll("[data-oi-i18n-aria-label]").forEach((element) => {
    element.setAttribute("aria-label", oiT(element.dataset.oiI18nAriaLabel));
  });
}

function titleCase(value) {
  return String(value || "").replace(/[-_]/g, " ").replace(/\b\w/g, (character) => character.toUpperCase());
}
